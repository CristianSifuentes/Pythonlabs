# Create Number, String, List variables
# Number

# Integer variable
# create integer variable
age = 28
print(age)  # 28
print(type(age))  # <class 'int'>

# Float variable
# create float variable
salary = 10800.55
print(salary)  # 10800.55
print(type(salary))  # <class 'float'>

# Complex type
'''
The complex is the numbers that come with the real and imaginary part.
 A complex number is in the form of a+bj, 
 where a and b contain integers or floating-point values.

'''
a = 3 + 5j
print(a)  # (3+5j)
print(type(a))  # <class 'complex'>